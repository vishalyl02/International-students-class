


#include<stdio.h>

int main()
{
    int a=10,b=20;
    
    //Addition operation  + -> this symbol is known as addition operator
    printf("Addition operation of %d and %d is: %d\n",a,b,a+b);
    
    // subtraction operation
    
    printf("Subtraction operation of %d and %d is %d\n",a,b,a-b);
    
    // Multiplication operatiuon
    
    printf("Multiplication operation of %d and % d is %d\n",a,b,a*b);
    
    // Divison operation
    printf("Division operation of %d and %d is %d\n",a,b,a/b);
    
    // Greater than operation  a=10, b=20
    
    printf("Greater than operation of %d and %d is %d\n",a,b,a>b);
    
    // Less than operation
    
    printf("Less than operation of %d and %d is %d\n",a,b,a<b);
    
    return 0;
}